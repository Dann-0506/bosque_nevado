package com.dan.animacion.models;

public class Poblado {
    public final float x, z, radio, radioTransicion, altura;

    public Poblado(float x, float z, float radio, float radioTransicion, float altura) {
        this.x = x;
        this.z = z;
        this.radio = radio;
        this.radioTransicion = radioTransicion;
        this.altura = altura;
    }
}
