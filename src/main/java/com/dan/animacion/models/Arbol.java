package com.dan.animacion.models;

public class Arbol {
    public final float x, y, z, escala, offsetViento;

    public Arbol(float x, float y, float z, float escala, float offsetViento) {
        this.x = x;
        this.y = y;
        this.z = z;
        this.escala = escala;
        this.offsetViento = offsetViento;
    }
}
